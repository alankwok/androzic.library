package com.androzic;

public class Rectangle
{
	public double left;
	public double top;
	public double right;
	public double bottom;

	public Rectangle()
	{
		left = 0;
		top = 0;
		right = 0;
		bottom = 0;
	}

	public Rectangle(double left, double top, double right, double bottom)
	{
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}
}
